    public void preparar(){
        try{
            this.sesion = repoSig.searchBy(Conector.getUsr());
            this.BienvenidaLbl.setText("Bienvenido " + sesion.primNombre + " " + sesion.segNombre);
        }
        catch (Exception e){
            e.printStackTrace();
            this.BienvenidaLbl.setText("Bienvenid@");
        }
    }
